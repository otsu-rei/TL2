#include <cstdint>
#include <iostream>
#include <format>

////////////////////////////////////////////////////////////////////////////////////////////
// methods
////////////////////////////////////////////////////////////////////////////////////////////

static void Pouse() {
	std::cout << "Press Enter to continue..." << std::endl;
	std::cin.get();
}

////////////////////////////////////////////////////////////////////////////////////////////
// main
////////////////////////////////////////////////////////////////////////////////////////////
int main(int argc, char* argv[]) {

	for (int i = 0; i < argc; ++i) {
		std::cout << std::format("argv[{}] : {}", i, argv[i]) << std::endl;
	}

	Pouse();
	return 0;

}
